n = int(input("Enter a number: "))
if 70 <= n and n <= 100:
    print("A")
elif 60 <= n and n <= 69:
    print("B")
elif 55 <= n and n <= 59:
    print("C")
elif 50 <= n and n <= 54:
    print("D")
elif 45 <= n and n <= 49:
    print("E")
elif 35 <= n and n <= 44:
    print("S")
elif 0 <= n and n <= 34:
    print("U")
else:
    print("Invalid Number!")
